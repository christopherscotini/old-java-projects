package br.com.todo.gpes.model.cadastro;

public class CentroCusto {
	
	//ESBOÇO

	private Integer id;
	private String centroCusto;
	private String descricao;
	private Integer torreDeNegocio;
	private Integer diretoria;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCentroCusto() {
		return centroCusto;
	}
	public void setCentroCusto(String centroCusto) {
		this.centroCusto = centroCusto;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public Integer getTorreDeNegocio() {
		return torreDeNegocio;
	}
	public void setTorreDeNegocio(Integer torreDeNegocio) {
		this.torreDeNegocio = torreDeNegocio;
	}
	public Integer getDiretoria() {
		return diretoria;
	}
	public void setDiretoria(Integer diretoria) {
		this.diretoria = diretoria;
	}
	

}
