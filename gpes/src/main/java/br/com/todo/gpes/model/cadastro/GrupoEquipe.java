package br.com.todo.gpes.model.cadastro;

public class GrupoEquipe {
	
	private Integer codGrupoEquipe;
	private String nomeDepartamento;
	private String descSecao;
	private String descGrupoEquipe;
	private String status;
	private String codJuncao;
	private String complemento;
	private Integer dataAtivacao;
	private Integer dataInativacao;
	
	
	
	
	
	public Integer getCodGrupoEquipe() {
		return codGrupoEquipe;
	}
	public void setCodGrupoEquipe(Integer codGrupoEquipe) {
		this.codGrupoEquipe = codGrupoEquipe;
	}
	public String getNomeDepartamento() {
		return nomeDepartamento;
	}
	public void setNomeDepartamento(String nomeDepartamento) {
		this.nomeDepartamento = nomeDepartamento;
	}
	public String getDescSecao() {
		return descSecao;
	}
	public void setDescSecao(String descSecao) {
		this.descSecao = descSecao;
	}
	public String getDescGrupoEquipe() {
		return descGrupoEquipe;
	}
	public void setDescGrupoEquipe(String descGrupoEquipe) {
		this.descGrupoEquipe = descGrupoEquipe;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCodJuncao() {
		return codJuncao;
	}
	public void setCodJuncao(String codJuncao) {
		this.codJuncao = codJuncao;
	}
	public String getComplemento() {
		return complemento;
	}
	public void setComplemento(String complemento) {
		this.complemento = complemento;
	}
	public Integer getDataAtivacao() {
		return dataAtivacao;
	}
	public void setDataAtivacao(Integer dataAtivacao) {
		this.dataAtivacao = dataAtivacao;
	}
	public Integer getDataInativacao() {
		return dataInativacao;
	}
	public void setDataInativacao(Integer dataInativacao) {
		this.dataInativacao = dataInativacao;
	}
	
	
	
}
